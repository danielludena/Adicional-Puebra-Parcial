    private void guardarClienteYCelular() {
    try {
        // Obtener datos de los campos
        String cedula = jTextField1.getText();
        String apellidos = jTextField2.getText();
        String nombres = jTextField3.getText();
        String numero = jTextField5.getText();
        double saldo = Double.parseDouble(jTextField6.getText());
        String estado = jTextField7.getText();
        int megas = Integer.parseInt(jTextField8.getText());

        // Crear objetos
        Cliente objCliente = new Cliente(cedula, apellidos, nombres);
        Celular objCelular = new Celular(numero, saldo, estado, megas, cedula);

        // Insertar en la base de datos
        logCliente.InsertarCliente(objCliente);
        logCelular.InsertarCelular(objCelular);

        // Confirmaci√≥n
        javax.swing.JOptionPane.showMessageDialog(this, "Datos guardados correctamente.");

        // Limpiar campos (opcional)
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField5.setText("");
        jTextField6.setText("");
        jTextField7.setText("");
        jTextField8.setText("");

    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error al guardar: " + e.getMessage());
    }
}

    
